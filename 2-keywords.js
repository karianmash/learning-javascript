// Common JavaScript keywords
// --------------------------------------------------------

// Keywords are reserved words that cannot be used as variable names, function names, or any other identifier names.

// The following are the list of JavaScript keywords:

/**
 *  break, case, catch, continue, debugger, default, delete, do, else, finally, for, function, if, in,
 * instanceof, new, return, switch, this, throw, try, typeof, var, void, while, with
 * class, const, enum, export, extends, import, super
 * implements, interface, let, package, private, protected, public, static, yield
 * null, true, false
 * */

// (a) break is used to break out of a loop or switch statement and continue execution at the next statement.
// (b) case is used to define individual cases in a switch statement.
// (c) catch is used to handle errors that occur within a try block.
// (d) continue is used to skip to the next iteration of a loop.
// (e) debugger is used to pause the execution of JavaScript code and launch the debugger.
// (f) default is used in a switch statement to define the default case.
// (g) delete is used to delete a property from an object.
// (h) do is used to create a do-while loop.
// (i) else is used in an if statement to specify what happens if the condition is false.
// (j) finally is used in a try-catch statement to specify code that should run regardless of whether an error was thrown.
// (k) for is used to create a for loop.
// (l) function is used to define a function.
// (m) if is used to create an if statement.
// (n) in is used to check if a property exists in an object.
// (o) instanceof is used to check if an object is an instance of a specific class.
// (p) new is used to create a new instance of a class.
// (q) return is used to return a value from a function.
// (r) switch is used to create a switch statement.
// (s) this is used to refer to the current object.
// (t) throw is used to throw an error.
// (u) try is used to create a try-catch statement.
// (v) typeof is used to check the type of a variable.
// (w) var is used to declare a variable.
// (x) void is used to evaluate an expression and return undefined.
// (y) while is used to create a while loop.
// (z) with is used to create a with statement.

// Related to object-oriented programming, module importing/exporting, and more

// 1. `class` is used to create a class.
// 2. `const` is used to declare a constant.
// 3. `enum` is used to create an enum.
// 4. `export` is used to export a module.
// 5. `extends` is used to create a subclass.
// 6. `import` is used to import a module.
// 7. `super` is used to refer to the parent class.
// 8. `implements` is used to create an interface.
// 9. `interface` is used to create an interface.
// 10. `let` is used to declare a variable.
// 11. `package` is used to create a package.
// 12. `private` is used to create a private member.
// 13. `protected` is used to create a protected member.
// 14. `public` is used to create a public member.
// 15. `static` is used to create a static member.
// 16. `yield` is used to create a generator.

// (qq) null is used to represent a null value.
// (rr) true is used to represent a true value.
// (ss) false is used to represent a false value.

